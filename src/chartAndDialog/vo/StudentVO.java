package chartAndDialog.vo;

public class StudentVO {
	private String stu_name;
	private int stu_kor;
	private int stu_mat;
	private int stu_eng;
	public String getStu_name() {
		return stu_name;
	}
	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}
	public int getStu_kor() {
		return stu_kor;
	}
	public void setStu_kor(int stu_kor) {
		this.stu_kor = stu_kor;
	}
	public int getStu_mat() {
		return stu_mat;
	}
	public void setStu_mat(int stu_mat) {
		this.stu_mat = stu_mat;
	}
	public int getStu_eng() {
		return stu_eng;
	}
	public void setStu_eng(int stu_eng) {
		this.stu_eng = stu_eng;
	}
	
}
